void solve(){
    ll a , b;
    cin >> a >> b;
    if(a==b){
        cout << 1 << "\n";
        return;
    }else {
        ll ans = 1;
        ll df = b -a ;
        if(df >= 10){
            cout << 0 << "\n";
            return;
        }
        for (int i = 1; i <= df; i++)
        {
            ans = ans *((a+i)%10);
        }
        cout << ans % 10 << "\n";
    }
    
}   
